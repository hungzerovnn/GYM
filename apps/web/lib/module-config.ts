export const getAllMenuItems = () => menuGroups.flatMap((group) => group.items);

export const getMenuItemByPath = (pathname: string) => {
  const directItem = getAllMenuItems().find((item) => isMenuItemActive(pathname, item.href));
  if (directItem) return directItem;

  const pageKey = pathname.replace(/^\//, "");
  const page = portalPageRegistry[pageKey];

  if (page?.title) {
    return {
      label: page.title,
      href: pathname,
      description: page.subtitle,
    };
  }

  return getMenuGroupByPath(pathname)?.items[0];
};

export const getViewPermissionForPath = (pathname: string) => {
  const slug = pathname.replace(/^\//, "").split("/").filter(Boolean);
  const page = resolvePortalPage(slug);

  if (!page) {
    return undefined;
  }

  if (page.kind === "dashboard") {
    return "dashboard.view";
  }

  if (page.kind === "setting") {
    return "settings.view";
  }

  if (page.kind === "resource") {
    return `${page.resource.permissionPrefix}.view`;
  }

  return page.report.permission || "reports.view";
};

export const resolvePortalPage = (slug: string[]) => {
  const key = slug.filter(Boolean).join("/") || "dashboard";
  const page = portalPageRegistry[key];

  if (!page) return null;

  if (page.kind === "dashboard") {
    return {
      kind: "dashboard" as const,
      title: page.title || "Dashboard tong quan",
      subtitle: page.subtitle || "Tong hop van hanh he thong.",
    };
  }

  if (page.kind === "resource" && page.resourceKey) {
    const pageOverrides = resourcePageOverrides[page.key] || {};
    return {
      kind: "resource" as const,
      resource: cloneResourceDefinition(page.key, page.resourceKey, {
        ...pageOverrides,
        title: page.title || pageOverrides.title,
        subtitle: page.subtitle || pageOverrides.subtitle,
      }),
    };
  }

  if (page.kind === "report" && page.reportKey) {
    return {
      kind: "report" as const,
      report: cloneReportDefinition(page.key, page.reportKey, {
        title: page.title,
        subtitle: page.subtitle,
      }),
    };
  }

  if (page.kind === "setting" && page.settingKey) {
    const pageOverrides = settingPageOverrides[page.key] || {};
    return {
      kind: "setting" as const,
      setting: cloneSettingDefinition(page.key, page.settingKey, {
        ...pageOverrides,
        title: page.title,
        subtitle: page.subtitle,
      }),
    };
  }

  return null;
};
